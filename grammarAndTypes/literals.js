/*
A literal represents a fixed value that we as developers insert into the code ourself.

-It is LITERALLY the code that we type, not data imported from elsewhere
*/

//string literal
let car = "Ford"

//numeric literal
let December = 12

//boolean literal
let tired = true

//object literal
let soup = {
    a: "chicken noodle",
    b: "tomato",
    c: "beef and barley"
}

//array literal
let days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"];

//Thats it for literals.  IT IS LITERALLY WHAT YOU VALUES YOU ASSIGN IN CODE!!!!!!!!!!!!